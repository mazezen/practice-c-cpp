#include <stdio.h>

int main()
{
    double a, b, c, d, e, f;

    a = 4.7;
    b = 4.;
    c = 4;
    d = .7;
    e = 0.7;
    f = 0;
    printf("a = %.2f\n", a);
    printf("b = %.2f\n", b);
    printf("c = %.2f\n", c);
    printf("d = %.2f\n", d);
    printf("e = %.2f\n", e);
    printf("f = %.2f\n", f);

    double x, y;
    x = 5e2;
    y = 5e-2;
    printf("x = %f\n", x);
    printf("y = %f\n", y);

    return 0;
}