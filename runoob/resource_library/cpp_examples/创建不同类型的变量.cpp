#include <iostream>
#include <string>

using namespace std;

int main()
{   
    int a = 10;
    char b = 'B';
    float c = 2.0f;
    double d = 10.0;
    bool flag = true;
    string str = "hello world";

    cout << "int: " << a << '\n';
    cout << "char: " << b << '\n';
    cout << "float: " << c << '\n';
    cout << "double: " << d << '\n';
    cout << "bool: " << flag << '\n';
    cout << "string: " << str << '\n';

    return 0;
}
